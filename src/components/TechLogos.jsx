export function getTechLogo(name, className = "w-5 h-5") {
  const normalized = name.toLowerCase().trim().replace(/[^a-z0-9]/g, "");
  switch (normalized) {
    case "postgresql":
      return <PostgreSQLIcon className={className} />;
    case "mysql":
      return <MySQLIcon className={className} />;
    case "mongodb":
      return <MongoDBIcon className={className} />;
    case "redis":
      return <RedisIcon className={className} />;
    case "clickhouse":
      return <ClickHouseIcon className={className} />;
    case "awscloudwatch":
    case "cloudwatch":
      return <AWSCloudWatchIcon className={className} />;
    case "elasticsearch":
      return <ElasticsearchIcon className={className} />;
    case "googleanalytics":
      return <GoogleAnalyticsIcon className={className} />;
    case "apachekafka":
    case "kafka":
      return <ApacheKafkaIcon className={className} />;
    case "kubernetes":
      return <KubernetesIcon className={className} />;
    case "multicloudecs":
    case "ecs":
      return <MultiCloudECSIcon className={className} />;
    case "containerapps":
      return <ContainerAppsIcon className={className} />;
    case "github":
      return <GitHubIcon className={className} />;
    case "gitlab":
      return <GitLabIcon className={className} />;
    case "bitbucket":
      return <BitbucketIcon className={className} />;
    case "slack":
      return <SlackIcon className={className} />;
    case "teams":
      return <TeamsIcon className={className} />;
    case "datadog":
      return <DatadogIcon className={className} />;
    case "newrelic":
      return <NewRelicIcon className={className} />;
    default:
      return null;
  }
}
